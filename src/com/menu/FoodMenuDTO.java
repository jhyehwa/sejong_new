package com.menu;

public class FoodMenuDTO {
	private int f_num;
	private String f_name;
	private String f_price;
	private int date;
	private String f_type;
	private String f_intro;
	private String f_image;

	public int getF_num() {
		return f_num;
	}

	public void setF_num(int f_num) {
		this.f_num = f_num;
	}

	public String getF_name() {
		return f_name;
	}

	public void setF_name(String f_name) {
		this.f_name = f_name;
	}

	public String getF_price() {
		return f_price;
	}

	public void setF_price(String f_price) {
		this.f_price = f_price;
	}

	public int getDate() {
		return date;
	}

	public void setDate(int date) {
		this.date = date;
	}

	public String getF_type() {
		return f_type;
	}

	public void setF_type(String f_type) {
		this.f_type = f_type;
	}

	public String getF_intro() {
		return f_intro;
	}

	public void setF_intro(String f_intro) {
		this.f_intro = f_intro;
	}

	public String getF_image() {
		return f_image;
	}

	public void setF_image(String f_image) {
		this.f_image = f_image;
	}

}
